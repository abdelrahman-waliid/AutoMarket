namespace CarMarketplace.Domain.Enums;

/// <summary>
/// Represents the transmission type of a car.
/// </summary>
public enum TransmissionType
{
    /// <summary>
    /// Manual transmission.
    /// </summary>
    Manual = 0,

    /// <summary>
    /// Automatic transmission.
    /// </summary>
    Automatic = 1,

    /// <summary>
    /// Continuously variable transmission.
    /// </summary>
    CVT = 2,

    /// <summary>
    /// Semi-automatic transmission.
    /// </summary>
    SemiAutomatic = 3
}
